    <p>For well over a decade, the US has been at war in Afghanistan and in Iraq. No matter the views anyone may have had when we invaded those countries, it is difficult to see how these nations will ever be stable enough for the US to pull out. This is not the fault of the US, and it is not a problem that we can fix.</p>

    <p>It is time to come home. As long as we protect our borders, the people of Afghanistan, Iraq, and the surrounding nations are no threat to anyone in our country. Rather than maintain military forces there, we should be protecting our borders here.</p>

    <p>At the same time as we are removing ourselves from those lands, the ideologies that are causing the wars there must be removed from the US. We are under no obligation to become a partially Afghan or Iraqi culture. This would be true even without the threat of terrorism or the views of Islam towards women and children, but the fact that these cultures are so incompatible with American culture makes their removal necessary for our safety.</p>



    <div class="fancy-title nobottommargin">
        <h3>Stand with Europeans</h3>
    </div>

    <div class="divider divider-border divider-right notopmargin">
        <i class="icon-world"></i>
    </div>


    <p>The European continent is currently being invaded and subjugated by African and middle-eastern peoples. They are suffering from terrorist attacks, from a massive rape epidemic, and their welfare systems are stretched beyond the breaking point. This is not the movement of refugees out of Syria, but rather conquest.</p>

    <p>We must stand with the European people as they fight this invasion. We must ensure that these cultures - whose histories make up most of the known world's history - continue to exist.</p>

    <p>To accomplish this, the US must turn back any smuggling boat bound for Europe. It must remove financial support to any country that assists the migrants in reaching Europe but refuses to take in migrants themselves. We must also ensure that the nations that have refused to let in the aggressors are not forced to surrender. Finally, it must fight for the freedom of the people within Europe who are imprisoned for speaking out against the occupation.</p>


