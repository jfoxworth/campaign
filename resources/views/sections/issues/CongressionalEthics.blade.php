	<div class="fancy-title nobottommargin">
		<h3>No Special Services</h3>
	</div>

	<div class="divider divider-border divider-right notopmargin">
		<i class="icon-settings2"></i>
	</div>

	<p>Nothing is in greater opposition to the ethical principles on which this nation was founded than for our lawmakers to create laws to which they themselves are exempt.</p>

	<p>While I am opposed to the government control of health care, Congress must be covered by any plan they enact. This is not something that should really be in debate and it shows just how far the divide between Congress and the people has gotten.</p>

	<p>Although Congress has taken action to reign in some of the more agregious insider trading, more action needs to be taken to stop the practice of representatives enriching themselves through their office.</p>

	<p>In addition to these reforms, an aggressive investigation into the wealth of every congressional member must be undertaken. That investigation should start with members that managed to accrue millions of dollars while their only source of income was their congressional salary.</p>











	<div class="fancy-title nobottommargin">
		<h3>No Special Protections</h3>
	</div>

	<div class="divider divider-border divider-right notopmargin">
		<i class="icon-settings2"></i>
	</div>

	<p>In response to widespread reporting of insider trading, Congress enacted the STOCK act in 2012. It was later "fixed" to close loop holes. The truth is that no special law was needed as Congress is not exempt from standard insider trading laws.</p>

	<p>A yearly audit of each representatives trading actions must be undertaken. This is to include their extended family as well. If any member made significant earnings in areas where they had influence then it must be considered a crime.</p>

	<p>No congressional member or staffer can be allowed to participate in any stock option not avaialble to the general public.</p>









	<div class="fancy-title nobottommargin">
		<h3>End Paid Lobbying</h3>
	</div>

	<div class="divider divider-border divider-right notopmargin">
		<i class="icon-settings2"></i>
	</div>

	<p>I will submit legislation to make it illegal for a paid representative to interact with a Congressman or their staff for the purposes of conveying information or influencing their opinion.</p>

	<p>Instead, I will task the federal government with building a web based system where lobbies and companies can convey information to Congressmen and their staff in whatever form they desire - documentation, presentations, text chats, or video conferencing. All of these interactions will then be available to the public.</p>












	<div class="fancy-title nobottommargin">
		<h3>A New Era in Oversight</h3>
	</div>

	<div class="divider divider-border divider-right notopmargin">
		<i class="icon-settings2"></i>
	</div>

	<p>To end the type of corruption that led to the STOCK Act, a new era of congressional oversight is needed. This will include increased scrutiny of the wealth accrued by members of Congress and their families.</p>

	<p>First, the current reports on assets for Congressmen is too vague. Congressional members must be required to report all assets accurate to within 10%. Second, the assets and activity of any family members must also be reported and investigated. Third, an aggressive investigation into the wealth of Congressional leadership must be undertaken. The practice of Congressional representatives becoming millionaires must be ended.</p>

