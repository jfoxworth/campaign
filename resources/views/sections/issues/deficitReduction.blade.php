    <div class="fancy-title nobottommargin topmargin">
        <h3>Immigration is largest drive of costs</h3>
    </div>



    <p>The dramatic expansion of both legal and illegal immigration into the US over the past few decades has driven an equally dramatic rise in the need for services. However, this same increase in immigration has driven down wages and placed a higher percentage of people below the line where they are now paying less into the system than they are taking out of it. Ending illegal immigration would lower costs, increase wages, and result in a massive upswing in the amount of money being brought in per person within the US.</p>


    <p>The legal immigration system must also be reformed into one that does not upset this balance within America. A dramatic reduction in the numbers of legal immigrants coupled with ensuring that only the best come into the nation will help reduce the deficit.</p>




   



    <div class="fancy-title nobottommargin topmargin">
        <h3>Ending affirmative action</h3>
    </div>


    <p>Affirmative action and the associated bureacracies are massive direct and indirect drains on the US economy. Ending these policies would result in a shift in the economy towards greater production and efficiency. Eliminating the associated departments at the state and federal levels would result in much lower costs. However, the true effects of this would not be seen immediately and it would take some time for the inefficiencies within our systems to work out. The potential savings coupled with the potential economic upswing could make this a big step in reducing our deficit.</p>













   



    <div class="fancy-title nobottommargin topmargin">
        <h3>Repurpose the military</h3>
    </div>


    <p>The amount of money that the US spends on our military is not sustainable and not needed. We should withdraw our forces from Iraq and Iran and place our military on our southern border. We should reduce the number of US bases overseas and focus on retaining the highest quality Solders, Sailors, Airmen, and Marines. We should focus on advancing our military technology and eliminating our dependence on Chinese products.</p>

    <p>The best defensive strategy for our nation is to ensure that OUR children are being educated in OUR universities - so that future generations have the leadership they need to sustain our civilization.</p>











   



    <div class="fancy-title nobottommargin topmargin">
        <h3>End unneeded bureacracies</h3>
    </div>


    <p>With the border secure and the threats to our nation isolated, the need to monitor the activity of every US citizen is no longer valid. We can eliminate the TSA and other PATRIOT Act holdover departments. The cost savings from this is not massive in terms of percentage of the deficit, however we must begin the process of ensuring that once a governmental department is created, it then lasts forever.</p>














   



    <div class="fancy-title nobottommargin topmargin">
        <h3>Address prescription drugs</h3>
    </div>


    <p>The United States allows pharmaceutical companies to sell drugs overseas at a fraction of the cost of what they are sold for here in the US. Eliminating this will lower the short and long term costs for US health care in the US. Allowing the US government to negotiate lower prices would also help. One of the primary drivers of our unfunded liabilities are these prescription drug costs. Addressing them will greatly improve our prosects for the future.</p>

    <p>We should also ensure that we have as many doctors as possible by keeping our medical schools for our children and not educating doctors that will not remain in the US. This will increase research in the US and lower health care costs for services.</p>















    <div class="fancy-title nobottommargin topmargin">
        <h3>Audit the FED and end QE</h3>
    </div>


    <p>In the early 1900's, the US government essentially lost control over much of its monetary policy to the newly created Federal Reserve. I would push to audit and end that private group and return control to congress. We must also end the practice of quantitative easing.</p>

    <p>Both the federal reserve and QE are things that are designed to hide our debt and increase taxation through devaluation of the currency. This devaluation helps us pay down our debts, but it also makes an unstable base for the rest of the economy and greatly lowers the ability of those on fixed income to survive - which increases our liabilities both in terms of what we must pay to individuals as well as how many individuals need assistance.</p>


















