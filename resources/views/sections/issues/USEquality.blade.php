    <p>Our culture has a right to exist. We have a right to self determination. We have a right to deny immigration to any and all people we see fit and to repatriate any who are present as the result of entering illegally. To question these rights is to question our right to equality.</p>


    <div class="fancy-title nobottommargin">
        <h3>Immigration history</h3>
    </div>

    <div class="divider divider-border divider-right notopmargin">
        <i class="icon-flag-alt"></i>
    </div>

    <p>In 1965, the US was 84% white. That same year, Congress passed the Immigration and Naturalization Act of 1965, also known as the Hart-Celler Act. The legislation changed US immigration policy away from focusing on European immigrants and towards others. In discussing the act, Senator Ted Kennedy asserted that "our cities will not be flooded with a million immigrants annually. ... Secondly, the ethnic mix of this country will not be upset." This promise was not honored and millions of both legal and illegal immigrants poured over the border from nations not previously allowed.</p>

    <blockquote>
        <p>our cities will not be flooded with a million immigrants annually. ... Secondly, the ethnic mix of this country will not be upset.</p>
        <footer>Senator Ted Kennedy - 1965 Immigration Act</footer>
    </blockquote>

    <p>Twenty years later, the American people demanded that something be done to end the unprecedented levels of legal and illegal immigration into their country. In 1986, President Ronald Reagan signed the Immigration Reform and Control Act, also known as the Simpsonâ€“Mazzoli Act. The act was a "grand bargain" where all illegl aliens currently present in the US were given citizenship with the promise that no more would be allowed to enter and US laws would be enforced.</p>

    <p>This again was another lie to the American people and the borders were left wide open to allow in tens of millions more illegal aliens. Since that time, every American president has campaigned on the promise of ending illegal immigration and every single one has acted in the exact opposite manner once elected.</p>

    <p>The history of the mass immigration of the last 50 years is one of illegal activity, corrupt politicians, and broken promises to the American people. The American people are under no moral nor legal obligation to grant citizenship to anyone who entered under such conditions - quite the opposite.</p>


    <div class="fancy-title nobottommargin">
        <h3>The damage done</h3>
    </div>

    <div class="divider divider-border divider-right notopmargin">
        <i class="icon-flag-alt"></i>
    </div>

    <p>The damage done to the culture has been catastrophic. In recent years, the amount of money sent to Mexico from workers in the US has surpassed the money brought into that country by oil revenue. This loss of financial capital to the US alone should be enough to spur action.</p>

    <p>In addition to this, <a href="https://cis.org/Welfare-Use-Legal-and-Illegal-Immigrant-Households">49% of households headed by legal immigrants</a> used one or more welfare programs in 2012, compared to 30 percent of households headed by natives. Households headed by legal immigrants have higher use rates than native households overall and for cash programs (14 percent vs. 10 percent), food programs (36 percent vs. 22 percent), and Medicaid (39 percent vs. 23 percent). Use of housing programs is similar. In addition to this, illegal immigrant households account for one quarter of all immigrant households accessing one or more welfare programs.</p>

    <p>US tax payers are being forced to pay for their own displacement. These payments to legal and illegal migrants is being carried out while veterans and senior citizens struggle with health, mental health, and financial security.</p>

    <p>In addition to this, the cost to house illegal aliens alone is close to $2 billion - or 1/10 the amount allocated to NASA.</p>

    <p>The cost to our culture has been much higher. Immigrants from nations to our south do not share the views of the native population on firearms, welfare, corruption, age of consent, or the size of government. In the next decade, they will have enough voters in Texas to control state wide elections. This means that the future of the nation will be drastically altered because of people who entered illegally or through direct deception of the American people. The white Christian native population will no longer have the right of self determination. All of this while the nations these migrants came from deny entry to any other people on the basis of the need to retain their culture and their national identity.</p>

    <p>What has happened in the US over the last 50 years is not migration. Migrants are invited and enter legally. This is an invasion.</p>



