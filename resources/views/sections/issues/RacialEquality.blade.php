    <div class="fancy-title title-bottom-border">
        <h3>An inherently unequal status</h3>
    </div>

    <p>It is tempting to say that equality in this country is often incorrectly viewed as equality of outcome. When the argument is made that one people group has more success than another and that this implies an inherent inequality in the system, we point out that we are guaranteed equality of opportunity and not outcome. This argument is tempting because it allows us to attach simple misconception to the motivations of those who want to assign preferences and punishments on the basis of race.</p>

    <p>The reality is that arguments for racial "equality" in this country have a singular target - white men. We often hear about the wage gap between blacks and whites as proof of racism, but we never discuss the fact that asians make more money than whites. There is never a call to "diversify" an Indian or Asian owned company. Only white people are told that their cities, their schools, their churches, their businesses, and the neighborhoods are not "diverse" enough. Only we are told that every company must have a diversity officer to ensure that there are not too many of us. Only we are told that there is a special "protected" class of people that must have special laws to shield them from our malice. Only we are told that our mere existence constitutes an inherent inequality that must be addressed through legislation, through preventing us from acting on our own behalf, and through preventing us from governing ourselves.</p>

    <p>These laws not only create a legal system that places white people at an institutional disadvantage, they provide the moral framework for broader hate crimes against our people. These crimes range from the obscene levels of violence visited upon white people in schools and on the streets to the countless numbers of jobs and educational opportunities that we are turned away from in favor of a more "diverse" candidate.</p>


    <blockquote>
        <p>These laws will never end, there will never be enough "equality", there will never be enough "diversity".</p>
        <footer>Joshua Foxworth</footer>
    </blockquote>


    <p>The longer we allow these laws to perpetuate, the more we weight we give to the argument that our existence constitutes an evil that requires government intervention. These laws will never end, there will never be enough "equality", there will never be enough "diversity". If you don't believe this, look at South Africa where the white population has been reduced to 9% of the population but massive affirmative action laws are enacted in favor of the black majority.</p>

    <p>These laws create a nobility class in America - one that is defined by race. These men and women are given special access to legal protection, special access to education, and preferential treatment in government contracts. No matter how much these people benefit from this government preference and no matter how much I am harmed, their children will still be able to claim preferential treatment on the basis that my children had "privilege".</p>


    <div class="fancy-title title-bottom-border">
        <h3>An end to affirmative action policies in education and hiring</h3>
    </div>

    <p>To any man that has children, no pain is greater than the realization that another man has the right to place his son ahead of mine. This is especially insulting when that man has been given preferential hiring over me; when his son has benefited beyond the capacity of his father's talent and work ethic; while I was only able to provide my son with what was left after the government gave a portion of my salary to the other man.</p>

    <p>How bad is this preferential treatment? A recent Princeton University study <a href="http://www.latimes.com/local/california/la-me-adv-asian-race-tutoring-20150222-story.html">found</a> that African Americans recieved a 230 point bonus on their SAT scores when considered for admission. Hispanics received a bonus of 185 points.</p>

    <p>Although I committed no crime, I have paid this debt throughout my entire life. My sons were not born into indebtedness. These laws must end.</p>


    <div class="fancy-title title-bottom-border">
        <h3>An end to preferential government contracting</h3>
    </div>

    <p>Government laws now mandate that a significant percentage of every contract go to minority owned businesses. This is not only an incredibly racist and unethical policy, it has destroyed large portions of government - including the space program.</p>

    <p>There are not enough true minority companies in the space program to meet the legal quotas mandated. This has resulted in the boom of "sub contracting" companies at NASA. These companies are owned by minorities and lay claim to 30% of the salaries of white male engineers. This is despite the fact that those engineers work at NASA buildings and rarely interact with their subcontracting companies. Those engineers cannot be promoted and have no rights. Most end up leaving the space program rather than spending their careers as under paid temp employees solely to justify racial quotas.</p>

    <p>These requirements must be immediately ended and the companies liquidated to provide reparations to the men whose labor the government stole.</p>



    <div class="fancy-title title-bottom-border">
        <h3>An end to preferential legal treatment</h3>
    </div>

    <p>In an effort to combat racism, laws have been enacted to ensure that any member of the "protected" class is not terminated improperly. This means that if the person sitting next to me is a member of the protected class, they can bring the full weight of the federal government against any privately owned employer even if no evidence of wrongdoing exists. On the other hand, I have no legal standing to sue an employer even if I have direct evidence of fraud and even if they employer was a government contractor.</p>

    <p>This is not equality.</p>




    <div class="fancy-title title-bottom-border">
        <h3>An end to racial punishment</h3>
    </div>

    <p>As part of his educational initiative, President Obama <a href="https://www.ed.gov/news/speeches/rethinking-school-discipline">asserted</a> that punishment differences in schools was not due to difference in behavior amongst the races, but rather due to inherent racism. He enacted policies that prevent African American students from being punished unless a similar number of white students had also been punished.</p>

    <p>These racist and bigoted rules have made our schools much more dangerous and made education almost impossible in some cases.</p>

    <p>In a similar but more insidious manner, many schools have begun to teach our children that they should be ashamed of their race and their heritage - that they should look in the mirror and hate who they are. These are practices that would have been judged as inhumane in even the most Orwellian of times. If you doubt that these views are being taught, look up the videos of college kids being forced to disavow their whiteness, or kids being forced to begin every presentation by apoligizing the native Americans for taking their lands. Look up the incidents at Evergreen college where a student yells out that whiteness is the most evil system that ever existed to thunderous applause.</p>

    <p>The federal government must immediately cease funding to and revoke the credentials of any university or grade school that teaches white children to hate themselves. These policies are divisive and unethical.</p>



