    <div class="fancy-title nobottommargin">
        <h3>Health Care and Migrants</h3>
    </div>

    <div class="divider divider-border divider-right notopmargin">
        <i class="icon-medkit"></i>
    </div>

    <div class="alignleft">
        <a href="#"><img src="/images/issues/healthcare.jpg"></a>
    </div>

    <p>The cost of health care for Americans has risen at a completely unsustainable rate with no end in sight. Increasingly complicated plans like "Obamacare" cannot reduce costs because they do not address the root causes of those rising costs. Indeed, no plan makes any effort to even name those causes.</p>

    <p>The cost of health care for Americans is directly related to the fact that our citizens subsidize both the health care of migrants within this nation, and the cost of prescription drugs in other nations. Ending these two practices will not only drastically reduce the cost of health care, but will also make it possible to understand what neeeds to be done to correct the system for Americans.</p>

    <p>To that end, free medical care for illegal aliens must be a thing of the past. If an illegal alien is injured, they will be treated and handed over to the authorities just like any other person who has violated the law. Legal aliens must be subject to the same scrutiny as Americans in determining whether or not they can pay for services rendered. We simply cannot morally tax our citizens or reduce services provided to seniors to pay for the cost of health care for citizens of other nations - including births.</p>

    <p>How much of a problem is this? In 2006, a Dallas hospital noted that <a href="http://www.snopes.com/politics/immigration/parkland.asp">70% of the women who gave birth in a 3 month span were illegal immigrants</a>. This means that US citizens are being taxed to pay for the costs of delivering the babies that are replacing them.</p>

    <p>Not one penny can go to the care of a citizen of another nation while veterans and senior citizens are either denied care or the cost of their care is increased.</p>


    <div class="fancy-title nobottommargin">
        <h3>Prescription Drug Costs</h3>
    </div>

    <div class="divider divider-border divider-right notopmargin">
        <i class="icon-medkit"></i>
    </div>

    <p>There is a great deal of discussion concerning the reimportation of medicine from other nations. The real question is why they are so much cheaper if purchased in Mexico? Why does the US government allow US companies to charge its citizens high rates and then sell that same medicine to other nations at drastically lower rates? Isn't that the exact opposite of how governments are supposed to function?</p>

    <p>We do not need a law allowing for reimportation of medicine. We simply need to remove the laws that allow companies to charge the citizens of our nation more than others.</p>


    <div class="fancy-title nobottommargin">
        <h3>A New Plan - No Government</h3>
    </div>

    <div class="divider divider-border divider-right notopmargin">
        <i class="icon-medkit"></i>
    </div>

    <p>Instead of discussing which party can better facilitate the administration of health care to all of America, we should simply end government's role in health care. This doesn't mean that we end all government programs, but it is not the government's role to decide who gets what health care or what level of taxation the American people will tolerate to provide care for those less fortunate.</p>


    <p>If government has any role in health care, it should be in assuring that procedures and medicines are safe and that there are no artificial limits on hospitals or doctors by organizations such as the AMA.</p>
    

