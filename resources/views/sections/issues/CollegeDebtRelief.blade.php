    <p>The cost of a university education has skyrocketed in recent decades. The discussion surrounding this crisis has focused on simply absolving the students of that debt with no consequences to the schools or to the students. This is wrong. We must address the causes of the rising costs to prevent future students from accruing this debt and we must address the debt already amassed.</p>

    <p>Federal guarantees on college debt was introduced to give poor students a chance to get a STEM education. It was never intended as a mechanism for Universities to charge hundreds of thousands of dollars for useless degrees. However, as wages have been driven down due to immigration and offshoring more and more kids have bought into the idea that they need a degree to make a living. The first step in solving the debt crisis is to address the societal causes that make it so difficult to support a family without a degree. An immediate end to the importation of both skilled and unskilled labor as well as policies that punish companies for offshoring work are good starts.</p>

    <p>In addition to this, the guarantee on non technical degreess should be removed. Universities that receive federal funding should also not be allowed to fund departments and degrees that have no purpose beyond the destruction of a cohesive society.</p>

    <p>With these policies in place, we can address the debt students currently have. On a student by student basis, we should ensure that graduates make good on the money they owe for the housing and care that they received while at Universities. However, they should be absolved of the debt that was accrued to pay for degrees that are worthless. This debt should not be passed on to the American taxpayer, but to the Universities that knowingly charged absurd rates for worthless degrees. The top 100 Universities have enough in endowements to pay for this debt.</p>

    <p>This <a href="https://www.americanthinker.com/articles/2019/07/nationalistic_college_debt_relief.html">nationalistic approach</a> holds the students accountable for their poor decisions, holds the Universities accountable for their malfeasance, and does not put the burden on the taxpayer who did not accrue the debt. It also begins to correct the use of Universities by foreign nations to push destructive policies into the American political sphere.</p>




