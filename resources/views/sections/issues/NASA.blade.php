    <p>Once the President has appointed the NASA leader, Congress must ensure a set amount of funds per year and then get out of the way. NASA leadership must be able to take those funds and save portions for years if necessary in preparation of certain phases of design, testing, and deployment, and it must be able to conduct its business where and how it sees fit.</p>

    <p>In short, Congress and NASA come to an agreement on how much is allocated per year and what they expect in terms of progress. NASA then goes away and does the work without Congress demanding the creation of or the ending of specific programs.</p>


    <div class="fancy-title nobottommargin">
        <h3>Structural and Ethical Reform</h3>
