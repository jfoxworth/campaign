    <p>NASA suffers from three main problems - a lack of direction, a lack of accountability, and corruption and fraud on several levels. The first part in turning that culture around is to ensure that it has a purpose that its employees can believe in and will work to fulfill.</p>

    <p>Although vague phrases like "Missions to Mars" are often used, the simple fact is that there is no legitimate plan to go to Mars or even to return to the moon. The space program is fractured, with multiple capsules being constructured by multiple companies and no real long term plan.</p>

    <p>NASA's purpose should be simple - a return to the moon and a manned mission to Mars. The technical steps for this goal must be up to the NASA leadership and not Congress. However, Congress must commit to reasonable funding goals, adhere to those goals, and then demand reasonable progress.</p>


    <div class="fancy-title nobottommargin">
        <h3>More Autonomy</h3>
    </div>

    <div class="divider divider-border divider-right notopmargin">
        <i class="icon-rocket"></i>
    </div>

    <p>The President must appoint a NASA leader that is willing to accept the responsibility of running the program. Congress must ensure a set amount of funds per year, and then get out of the way. NASA leadership must be able to take those funds and save them for years if necessary in preparation of certain phases of design, testing, and deployment, and it must be able to conduct its business where and how it sees fit.</p>


    <div class="fancy-title nobottommargin">
        <h3>Structural and Ethical Reform</h3>
    </div>

    <div class="divider divider-border divider-right notopmargin">
        <i class="icon-rocket"></i>
    </div>

    <p>At the current time, there are capsules being built by Lockheed Martin on a cost-plus contract, by Boeing on a fixed price contract, and by SpaceX, and several other companies. This is done under the guise of saving money. However, it has the result of costing a great deal more money and directly harming NASA's ability to move forward.</p>

    <p>NASA has its own bureaucracy. The involvement of these multiple companies creates a separate structure on top of that to sit between NASA, and the engineers at each company. The addition of the company's need to make money along with this bureaucracy adds cost.</p>

    <p>In addition to this, these companies hire engineers for projects and then lay them off at the completion of each project. Many of those engineers are required to broker their labor through minority owned businesses. This means that they have to give a significant portion of their salary to the minority owned company, can never be considered for promotion, and have no rights. The end result of this is that a white male with a Master's degree in Aeropsace Engineering is little more than a journeyman contractor. Good engineers then leave to find places where their talent and work is rewarded.</p>

    <p>Another cost of this model is the loss of institutional memory. Normally, new engineers would be assisted by older ones with experience in the space program. As new engineers gain experience, mistakes aren't repeated, old ideas aren't retried, and those engineers then mentor the next generation. With the termination of employment that now comes at the end of each program, that memory is lost. We are constantly paying for young engineers to relearn what we learned in the 60s and 70s.</p>

    <p>The solution to this is simple - end the contracting. Simply hire those engineers into NASA, pay them what they are worth, and retain them. This is cheaper and holds on to better engineers since they are receiving their full pay.</p>

    <p>NASA contractors currently have separate practices for hiring, evaluating, and firing white males. This must end as well.</p>

    <p>With these reforms in place, a greater level of accountability can be enacted.</p>

