    <p>At the current time, there are capsules being built by Lockheed Martin on a cost-plus contract, by Boeing on a fixed price contract, and by SpaceX, and several other companies. This is done under the guise of saving money. However, it has the result of costing a great deal more money and directly harming NASA's ability to move forward.</p>

    <p>NASA has its own bureaucracy. The involvement of these multiple companies creates a separate structure on top of that to sit between NASA and the engineers at each company. The addition of the company's need to make money, this bureaucracy, the bidding process, and contracting procedures all add costs.</p>

    <p>Added to this, these companies hire engineers for projects and then lay them off at the completion of each project. Many of those engineers are required to broker their labor through minority owned businesses. This means that they have to give a significant portion of their salary to the minority owned company, can never be considered for promotion, and have no rights. The end result of this is that a white male with a Master's degree in Aeropsace Engineering is little more than a journeyman contractor. Good engineers then leave to find places where their talent and work are rewarded.</p>

    <p>Another cost of this model is the loss of institutional memory. Normally, new engineers would be assisted by older ones with experience in the space program. As new engineers gain experience, mistakes aren't repeated, old ideas aren't retried, and those engineers then mentor the next generation. With the termination of employment that now comes at the end of each program, that memory is lost. We are constantly paying for young engineers to relearn what we learned in the 60s and 70s and there is little to no incentive for engineers to work harder or better to bring more money to a subcontracting company they never interact with.</p>

    <p>Consider this ... An engineer working for NASA is very talented and works a large number of hours. As a result of this, he accomplishes more for NASA, is promoted, and NASA is all the better for it. Currently, contracting companies actually make less money if that talented engineer completes a job in less hours than multiple less competent engineer. He is never recognized for his work and can never be promoted.</p>

    <p>The solution to this is simple - end the contracting. Simply hire those engineers into NASA, pay them what they are worth, and retain them. This is cheaper and holds on to better engineers since they are receiving their full pay.</p>

    <p>NASA contractors currently have separate practices for hiring, evaluating, and firing white males. This must end as well.</p>

    <p>With these reforms in place, a greater level of accountability can be enacted as NASA can interact directly with low performing engineers when they fall behind instead of dealing with lawyers only when milestones are missed.</p>

