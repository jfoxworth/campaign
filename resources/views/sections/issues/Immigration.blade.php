	</div>

	<p>America is one of the most technologically advanced nations on the planet. The assertion that we need technical workers from nations that are far less advanced and with inferior educational systems to sustain our economy is not something that can be factually supported. Programs such as the H1B and L1 visas are filled with fraud and abuse and are being used to displace and replace American workers. </p>

	<p>Legislation such HR 1044 and S386 have shown that these programs are footholds for foreign nationals who then redirect our entire immigration system. All guest worker programs must be ended and those workers who came here on these systems must return to their nations.</p>





