	<p>Nations have borders. If the people of other nations insist that we honor their borders and yet refuse to honor ours, then a physical barrier to prohibit illegal entry is both reasonable and effective. Any politican that opposes such a barrier for our nation but supports it for other nations is not acting in the best interest of the American people.</p>





	<div class="fancy-title nobottommargin">
		<h3>Rethinking Immigration Policies - Reciprocity</h3>
	</div>

