    <p>For well over a decade, the US has been at war in Afghanistan and in Iraq. No matter the views anyone may have had when we invaded those countries, it is difficult to see how these nations will ever be stable enough for the US to pull out. This is not the fault of the US, and it is not a problem that we can fix.</p>

    <p>It is time to come home. As long as we protect our borders, the people of Afghanistan, Iraq, and the surrounding nations are no threat to anyone in our country. Rather than maintain military forces there, we should be protecting our borders here.</p>

    <p>At the same time as we are removing ourselves from those lands, the ideologies that are causing the wars there must be removed from the US. We are under no obligation to become a partially Afghan or Iraqi culture. This would be true even without the threat of terrorism or the views of Islam towards women and children, but the fact that these cultures are so incompatible with American culture makes their removal necessary for our safety.</p>



