	    <p>In recent years, it has become fashionable to support the removal of any monument built to honor the memory of anyone associated with the Confederacy or with slavery. It is important to understand the genesis of this conflict and why it is happening.</p>

	    <p>Nations are built by people groups. As those groups share a history they build monuments for their descendents to see. The purpose of these monuments can be to honor those that fought in wars to defend the land those descendents stand on, or to make a permanent mark so that future generations understand their struggles and views. As a country's views change, their views of those monuments change but they rarely choose to remove those markers.</p>

	    <p>What is happening how is not the shifting of a people's view of their own history.</p>

	    <p>When the statues of the confederate leaders were built, they were built be the children and grandchildren of those men. The wanted their posterity to understand their views and their pain. The occupants of those cities are no longer the descendents of those people. Those descendents have been either pushed out or marginalized by the influx of other people. It is this new people group that is seeking to remove the history of the previous group.</p>

	    <p>As this new people group has moved into these areas, they do not wish to be reminded that there is a shared history of the land that does not include them. They do not want the original inhabitants to be reminded that the land was once theirs. Their solution is to label those monuments as hateful to provide the moral justification for removing. Parks and other areas named after these men are renamed and instead of history starting with the original people group, a new history is written that starts with the new people group. This is why parks are never renamed after a founding father or biblical icon, but rather a figure from modern times.</p>

	    <p>I oppose the destruction of these monuments and the history they represent. However, the removal of these monuments is a sympton of the larger issue - the displacement of the original inhabitants and culture of the land. If that practice is not stopped, the destruction of monuments and the remaking of history will continue past the civil war and will quickly include the founding fathers.</p>

